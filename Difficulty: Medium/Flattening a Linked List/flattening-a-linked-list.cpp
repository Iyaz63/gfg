class Solution {
 Node* merge(Node* a, Node* b){          // merge two sorted bottomâ€‘lists
        if(!a) return b;
        if(!b) return a;
        if(a->data < b->data){
            a->bottom = merge(a->bottom, b);
            a->next = nullptr;
            return a;
        }
        b->bottom = merge(a, b->bottom);
        b->next = nullptr;
        return b;
    }
public:
    Node *flatten(Node *root){
        if(!root || !root->next) return root;
        root->next = flatten(root->next);   // flatten rest of list
        return merge(root, root->next);     // merge current with flattend rest
    }
};



